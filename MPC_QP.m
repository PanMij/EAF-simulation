                                     C, D, wy, umin, umax, dumin, dumax, u_init, du_init, ...
                                     L, use_quadprog, mode, u_applied);
        else
            [du, info] = MPC_QP_core(y, r, A, B, N1, N2, Nu, alpha, lam, reset, ...
                                     C, D, wy, umin, umax, dumin, dumax, u_init, du_init, ...
                                     L, use_quadprog, mode, u_applied, solve_when_inactive);
        end
    else
        if nargin == 21
            du = MPC_QP_core(y, r, A, B, N1, N2, Nu, alpha, lam, reset, ...
                             C, D, wy, umin, umax, dumin, dumax, u_init, du_init, ...
                             L, use_quadprog);
