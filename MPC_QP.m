%   implementation to MPC_QP_core so Simulink and script callers can share
%   the same controller logic.

    if nargout > 1
        if nargin == 21
            [du, info] = MPC_QP_core(y, r, A, B, N1, N2, Nu, alpha, lam, reset, ...
